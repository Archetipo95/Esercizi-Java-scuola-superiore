public class Rettangolo
	{
		public double Cerchio;
			public double base,altezza;
			public Rettangolo()
			{}

			
			public double perimetro()
			{
				double pr;
				pr=(base+altezza)*2;
				return pr;
			}
				
			
			public double area()
			{
				double a;
				a=base*altezza;
				return a;
			}
	}


